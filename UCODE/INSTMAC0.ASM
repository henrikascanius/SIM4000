;**********************************************************************
;*****
;*****              RC8000 machine instructions, part 1
;*****
;**********************************************************************

        INCLUDE ADDRMACI.ASM
; am, al, ac, ic


;**********************************************************************
;*****
;*****                       Address Handling
;*****
;**********************************************************************

_am     MACRO wreg              ; Address Modify
        incicam
        ENDM

        MakeOp  am,9

;**********************************************************************

_al     MACRO wreg              ; Address Load
        mov     ds:[&wreg*4],eax
        incic
        ENDM
        
        MakeOp  al,11

;**********************************************************************

_ac     MACRO wreg              ; Address Complement
        neg     eax
        mov     ds:[&wreg*4],eax
        intexs
        incic
        ENDM

        MakeOp  ac,33

;**********************************************************************
;*****
;*****                       Monitor control:
;*****
;**********************************************************************

;**********************************************************************

_ic     MACRO wreg              ; Clear interrupt bits
        not     eax
        and     es:IR,eax
        incic
        ENDM

        MakeOp  ic,47
